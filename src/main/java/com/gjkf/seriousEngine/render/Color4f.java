package com.gjkf.seriousEngine.render;

public class Color4f{

    /**
     * Red component
     */
    public final float r;
    /**
     * Green component
     */
    public final float g;
    /**
     * Blue component
     */
    public final float b;
    /**
     * The alpha component
     */
    public final float a;

    public Color4f(float r, float g, float b, float a){
        this.r = r;
        this.g = g;
        this.b = b;
        this.a = a;
    }

}
