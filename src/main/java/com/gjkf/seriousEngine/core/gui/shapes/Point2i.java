package com.gjkf.seriousEngine.core.gui.shapes;

/**
 * A class representing a point
 */

public class Point2i{

    /**
     * The x Coordinate
     */
    public final int x;
    /**
     * The y coordinate
     */
    public final int y;

	public Point2i(int x, int y){
		this.x = x;
		this.y = y;
	}
}
