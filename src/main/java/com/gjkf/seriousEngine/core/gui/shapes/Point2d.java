package com.gjkf.seriousEngine.core.gui.shapes;

/**
 * Class representing a point.
 */

public class Point2d{

    /**
     * The x Coordinate
     */
    public final double x;
    /**
     * The y coordinate
     */
    public final double y;

    public Point2d(double x, double y){
        this.x = x;
        this.y = y;
    }

}
