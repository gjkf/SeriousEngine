package com.gjkf.seriousEngine.core.render;

public class Color3f{

    /**
     * Red component
     */
	public final float r;
    /**
     * Green component
     */
	public final float g;
    /**
     * Blue component
     */
	public final float b;

	public Color3f(float r, float g, float b){
		this.r = r;
		this.g = g;
		this.b = b;
	}
}
